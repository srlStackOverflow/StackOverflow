package readCsvFiles;

public class Answers {
	public long id;
	public int score;
	public String code;
	
	
	public Answers () {
		super();
		this.id = 0;
		this.score = 0;
		this.code =null;
		;
	}
	public Answers (long id, int score, String code) {
		super();
		this.id = id;
		this.score = score;
		this.code = code;
		;
	}
	
}
